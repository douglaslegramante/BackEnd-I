//Criando um objeto literal
var produto = {
    id: 9,
    nome: "Cafeteira Elétrica",
    valor: 99.00
};

//Recuperando os dados do objeto
console.log(produto.id); // 9
console.log(produto.nome); // Cafeteira Elétrica
console.log(produto.valor); // 99.00

